console.log("The mul of a * b is : " , a*b);
console.log("The div of a / b is : " , a/b);
console.log("The modulus of a % b is : " , a%b);

let z = 50;

// post increment
console.log(z++); // z = z + 1 => 51
console.log(z); // 51

// pre-increment
console.log(++z); // z = z + 1 => 51 +1 => 52
z+=20; // 72
console.log(z);

